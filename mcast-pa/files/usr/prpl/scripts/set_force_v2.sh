force=$(uci get iptv.@igmp[0].force_v2)

if [ "$force" = "" ]; then
	force=0
fi
# force V2 only.
if [ $force -ne 0 ]; then
	force=2
fi

echo $force > /proc/sys/net/ipv4/conf/all/force_igmp_version
